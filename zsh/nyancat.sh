#!/bin/bash


a='\033[0;104m  '     # ,
b='\033[0;107m  '     # .
c='\033[41m  '        # >
d='\033[0;101m  '     # &
e='\033[43m  '        # +
f='\033[0;102m  '     # #
g='\033[46m  '        # =
h='\033[45m  '        # :
i='\033[40m  '        # '
j='\033[47m  '        # *
k='\033[0;103m  '     # @
m='\033[45m  '        # -
l='\033[0;105m  '     # ^
n='\033[0;105m  '     # %

declare -a arr=(
    ",,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,"
    ",,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,"
    ",,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,"
    ">>>>>>>>>>>>''''''''''''''',,,,,,,,,"
    ">>>>>>>>>>>'@@@@@@@@@@@@@@@',,,,,,,,"
    ">>>>>>>>>>'@@@^^^^^^^^^^^@@@',,,,,,,"
    "&&&&&&&&&&'@@^^^^^-^^-^^^^@@',,,,,,,"
    "&&&&&&&&&&'@^^-^^^^^^''^-^^@','',,,,"
    "&&&&&&&&&&'@^^^^^^^^'**'^^^@''**',,,"
    "+++++'''++'@^^^^^-^^'***'^^@'***',,,"
    "++++'**''+'@^^^^^^^^'****'''****',,,"
    "+++++'**'''@^^^^^^^^'***********',,,"
    "#####''**''@^^^^^^-'*************',,"
    "######''**'@^-^^^^^'***.'****.'**',,"
    "#######''''@^^^^^^^'***''**'*''**',,"
    "=========''@@^^^-^^'*%%********%%',,"
    "=========='@@@^^^^^^'***''''''**',,,"
    "========='''@@@@@@@@@'*********',,,,"
    "::::::::'***''''''''''''''''''',,,,,"
    "::::::::'**'','*',,,,,'*','**',,,,,,"
    ",,,,,,,,'''',,'',,,,,,,'',,'',,,,,,,"
    ",,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,"
    ",,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,"
    ",,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,"
)

clear
for row in ${arr[@]}
do
    string="${row//\,/$a}"
    string="${string//\./$b}"
    string="${string//\>/$c}"
    string="${string//\&/$d}"
    string="${string//\+/$e}"
    string="${string//\#/$f}"
    string="${string//=/$g}"
    string="${string//\:/$h}"
    string="${string//\'/$i}"
    string="${string//\*/$j}"
    string="${string//\@/$k}"
    string="${string//-/$m}"
    string="${string//\^/$l}"
    string="${string//\%/$n}"
    echo -e "$string"
done
